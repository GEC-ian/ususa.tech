const referenceNo = document.getElementById("referenceNo");
const date = document.getElementById("date");
const studentName = document.getElementsByClassName("studentName");
const collegeName = document.getElementById("collegeName");
const companyName = document.getElementById("companyName");
const duration = document.getElementById("duration");
const work = document.getElementById("work");
const certificateContainer = document.getElementById("certificateContainer");
const certificateId = document.getElementById("certificateId");
const verifyButton = document.getElementById("verifyButton");

verifyButton.addEventListener("click", function () {
  const certificate = certificates.find(
    (certificate) => certificate.id === certificateId.value
  );
  if (certificate) {
    certificateContainer.style.display = "block";
    referenceNo.innerHTML = "UTS/INT/0" + certificate.referenceNum + "/2023";
    date.innerHTML = certificate.date;
    studentName[0].innerHTML = certificate.studentName;
    
    collegeName.innerHTML = certificate.collegeName;
    companyName.innerHTML = certificate.companyName;
    duration.innerHTML = certificate.duration;
    work.innerHTML = certificate.work;
    studentName[1].innerHTML = certificate.studentName;
  } else {
    alert("Invalid Certificate ID");
  }
});



function printCertificate() {
  var certificateContainer = document.getElementById("certificateContainer");
  var printButton = document.getElementById("printButton");
  certificateContainer.removeChild(printButton);
  var originalContents = document.body.innerHTML;
  document.body.innerHTML = certificateContainer.innerHTML;
  window.print();
  document.body.innerHTML = originalContents;
}

document.getElementById("printButton").addEventListener("click", printCertificate);

 
